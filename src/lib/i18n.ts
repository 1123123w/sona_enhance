const dictionaries: Record<AppLanguage, TranslationMap> = {
  'zh-CN': zhCN,
  'zh-TW': zhTW,
  'en-US': enUS,
}

export const languageOptions = [
  { value: 'zh-CN', labelKey: 'language.zhCN' },
  { value: 'zh-TW', labelKey: 'language.zhTW' },
  { value: 'en-US', labelKey: 'language.enUS' },
] as const

export function translate(language: AppLanguage, key: string, vars?: Record<string, string | number>): string {
  const template = dictionaries[language]?.[key] ?? zhCN[key] ?? key
  if (!vars) return template
  return template.replace(/\{(\w+)\}/g, (_, name) => String(vars[name] ?? `{${name}}`))
}

export function useI18n() {
  const [language, setLanguageState] = useState<AppLanguage>(() => store.get('language'))

  useEffect(() => store.onChange('language', setLanguageState), [])

  const setLanguage = (next: AppLanguage) => {
    setLanguageState(next)
    store.set('language', next)
  }

  return {
    language,
    setLanguage,
    t: (key: string, vars?: Record<string, string | number>) => translate(language, key, vars),
  }
}
